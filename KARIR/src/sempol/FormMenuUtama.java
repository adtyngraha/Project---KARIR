    public FormMenuUtama() {
    initComponents();
    setLocationRelativeTo(null);

    // Tampilkan nama & jabatan dari session
    String nama = Session.nama;
    String jabatan = Session.jabatan;
    jLabel2.setText("Selamat Datang : " + nama + " (" + jabatan + ")");

    // Atur hak akses sesuai jabatan
    aturAkses();

    // Tampilkan logo
    try {
        ImageIcon logoIcon = new ImageIcon(getClass().getResource("/gambar/logo.png"));
        Image image = logoIcon.getImage();
        Image newimg = image.getScaledInstance(700, 200, java.awt.Image.SCALE_SMOOTH); 
        logoIcon = new ImageIcon(newimg);
        lblLogo.setIcon(logoIcon);
    } catch (Exception e) {
        System.out.println("Error memuat gambar logo: " + e.getMessage());
    }
}

// Method untuk menyembunyikan menu admin jika login sebagai kasir
private void aturAkses() {
    if (Session.jabatan.equalsIgnoreCase("kasir")) {
        jMenu1.setVisible(false);  // sembunyikan menu admin
        // Kalau kamu punya tombol admin juga, tambahkan disable di sini
        // contoh: btnPegawai.setEnabled(false);
    }
}

// Jika form dimunculkan kembali, cek ulang akses
@Override
public void setVisible(boolean b) {
    super.setVisible(b);
    if (b) {
        jLabel2.setText(Session.nama + " (" + Session.jabatan + ")");
        aturAkses(); // Pastikan hak akses tetap sesuai saat form dibuka ulang
    }
}




