    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText().trim();
        String nama = jTextField2.getText().trim();
        String username = jTextField3.getText().trim();
        String rawPassword = new String(jPasswordField1.getPassword()).trim();
        String password = HashUtil.hashPassword(rawPassword);
        String jabatan = (String) jComboBox1.getSelectedItem();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong.");
            return;
        }
        try {
            Connection conn = DatabaseConnection.connect();
            String cekQuery = "SELECT COUNT(*) FROM pegawai WHERE username=?";
            PreparedStatement cekPst = conn.prepareStatement(cekQuery);
            cekPst.setString(1, id);
            ResultSet rs = cekPst.executeQuery();
            rs.next();
            
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Username sudah digunakan!");
                return;
            }
            String insertQuery = "INSERT INTO pegawai(id_pegawai, nama_pegawai, username, password, jabatan) VALUES(?,?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(insertQuery);
            pst.setString(1, id);
            pst.setString(2, nama);
            pst.setString(3, username);
            pst.setString(4, password);
            pst.setString(5, jabatan.toLowerCase());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jPasswordField1.setText("");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menyimpan:" + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

