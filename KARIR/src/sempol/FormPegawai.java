    private void tampilkanDataUser(){
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "SELECT id_pegawai, nama_pegawai, username, jabatan FROM pegawai ORDER BY id_pegawai ASC";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Nama");
            model.addColumn("Username");
            model.addColumn("Jabatan");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_pegawai"),
                    rs.getString("nama_pegawai"),
                    rs.getString("username"),
                    rs.getString("jabatan")
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    
    private void filterData(String Keyword){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "Select id_pegawai, nama_pegawai, username, jabatan FROM pegawai WHERE  nama_pegawai LIKE ? OR username LIKE ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            
            pst.setString(1, "%" + Keyword + "%");
            pst.setString(2, "%" + Keyword + "%");
            ResultSet rs = pst.executeQuery();
            while (rs.next()){
                model.addRow(new Object[]{
                    rs.getString("id_pegawai"),
                    rs.getString("nama_pegawai"),
                    rs.getString("username"),
                    rs.getString("jabatan")
                });
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memfilter data: " + e.getMessage());
        }
        
    }
    

